public class Aula1TiposDeVariaveis{
    public static void main (String[]args){
    String String = "Carlos";
    int Inteiro = 23;
    double Double = 1000;
    float Float = 250;
    boolean BooleanVerdadeiro = Float == 250;
    boolean BooleanFalso = Double == 500;
    char Char = 'Q';
    byte Byte = 127;
    long Long = 100000000;
    short Short = 20;

    System.out.println("String: : " + String);
    System.out.println("Inteiro: " + Inteiro);
    System.out.println("Double: " + "R$" + Double);
    System.out.println("Float: " + "R$" + Float);
    System.out.println("Boolean Verdadeiro: " + BooleanVerdadeiro);
    System.out.println("Boolean Falso: " + BooleanFalso);
    System.out.println("Char: " + Char);
    System.out.println("Byte: "+Byte);
    System.out.println("Long: "+Long);
    System.out.println("Short: "+Short);






    }

}